public class OddEven {

private int odds;
private int evens;

    public OddEven(int odds, int evens) {
        this.odds = odds;
        this.evens = evens;
    }

    public int getEvens() {
        return evens;
    }

    public void setEvens(int evens) {
        this.evens = evens;
    }

    public int getOdds() {
        return odds;
    }

    public void setOdds(int odds) {
        this.odds = odds;
    }
}
